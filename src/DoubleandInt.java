public class DoubleandInt {
    public static void main(String[]args){
        int O = 10 + 3 * 2;
        System.out.println(O);
        int K = 1;
        int J = K+2;
        System.out.println(J);
        double M = 1.6;
        double N = M + 2.3;
        System.out.println(N);
        //adding double and int
        double H = 1.1;
        int I = (int)H + 2;
        System.out.println(I);
    }
}
