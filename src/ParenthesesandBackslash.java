public class ParenthesesandBackslash {
    public static void main(String[]args){
        //putting a word in parentheses
        System.out.println("Hello \"jesse\"");
        // c:\Windows\...
        System.out.println("c:\\Windows\\...");
        System.out.println("c:\t Windows\\...");
        System.out.println("c:\nWindows\\");
    }
}
